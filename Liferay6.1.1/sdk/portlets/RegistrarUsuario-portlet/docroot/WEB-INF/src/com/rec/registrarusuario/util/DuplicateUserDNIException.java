package com.rec.registrarusuario.util;

public class DuplicateUserDNIException extends Exception {

	public DuplicateUserDNIException(){
		super();
	}
	
	
	public DuplicateUserDNIException(String arg0) {
		super(arg0);
	}

	public DuplicateUserDNIException(Throwable arg0) {
		super(arg0);
	}

	public DuplicateUserDNIException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
